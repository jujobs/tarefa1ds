public class TiposPrimitivos {

    public static void main(String[] args) { // é o começo do código e serve para guardar os valores das variáveis ao longo do código
        char sexo = 'f'; //declaração da variável "sexo"
        byte idade = 89; // declaração da variável "idade"
        short codigo = 256; // declaração da variável "codigo"
        float nota = 9.4f; // declaração da variável "nota"
        int alunos = 100, classes = 10; // declaração da variável "alunos"
        long habitantes = 55000; //declaração da variável "habitantes"
        double dolar = 4.70; //declaração da variável "dolar"
        boolean alternativa = false; //*declaração da variável "alternativa" ( boolean serve para especificar
        // se o valor da variável é verdadeiro ou falso

        System.out.println("Sexo: " + sexo + " / " + "Idade:" + idade + " / " + "Código: " + codigo); // imprime na tela o valor das variáveis em linha com separações
        System.out.println("Nota" + nota + " / " + "Alunos: " + alunos + " / " + "Classes: " + classes); // imprime o valor das variáveis em linha com separações
        System.out.println("Habitantes: " + habitantes + " / " + "Dolar: " + dolar + " / " + "alternativa: " + alternativa); // imprime na tela o valor das variáveis em linha com separações
    }
}


